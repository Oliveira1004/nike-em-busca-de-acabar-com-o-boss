function clear_game () {
    for (let value of sprites.allOfKind(SpriteKind.Enemy)) {
        sprites.destroy(value)
    }
    for (let value_1 of sprites.allOfKind(SpriteKind.boss)) {
        sprites.destroy(value_1)
    }
    for (let value_2 of sprites.allOfKind(SpriteKind.goal)) {
        sprites.destroy(value_2)
    }
    for (let value_3 of sprites.allOfKind(SpriteKind.coin)) {
        sprites.destroy(value_3)
    }
}
