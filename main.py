def on_on_overlap(sprite, otherSprite):
    if sprite.vy > 0 and not (sprite.is_hitting_tile(CollisionDirection.BOTTOM)) or sprite.y < otherSprite.top:
        sprites.destroy(otherSprite, effects.fire, 500)
        otherSprite.vy = -50
        info.change_score_by(1)
    else:
        info.change_score_by(-1)
        music.play(music.create_song(hex("""
                0078000408020105001c000f0a006400f4010a0000040000000000000000000000000000000002140000000400012a0400080002202708000c00021d24
                """)),
            music.PlaybackMode.UNTIL_DONE)
    pause(2000)
sprites.on_overlap(SpriteKind.player, SpriteKind.enemy, on_on_overlap)

